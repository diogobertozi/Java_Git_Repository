
abstract class ReinoAnimal {

	double altura;
	
	public void setAltura(double h)
	{
		altura = h;
	}
	
	public double getAltura()
	{
		return altura;
	}
	
	boolean chifre;
	
	public void setChifre(boolean c)
	{
		chifre = c;
	}
	
	public boolean getChifre()
	{
		return chifre;
	}
	
	int peso;
	
	public void setPeso(int p)
	{
		peso = p;
	}
	
	public int getPeso()
	{
		return peso;
	}
	
	String cor;
	
	public void setCor(String color)
	{
		cor = color;
	}
	
	public String getCor()
	{
		return cor;
	}
	
}
